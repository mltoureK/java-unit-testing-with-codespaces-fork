
public class Calculator {
  public int sum(int x, int y) {
    return x + y;
  }

  // This method return the result of subtracting y from x.
  public int subtracting(int x, int y) {
    return x - y;
  }

  // This method should returnn the result of multiplying x and y.
  public int multiplying(int x, int y) {
    return x * y;
  }

  public int dividing(int x, int y) {
    return x / y;
  }
  // This method should return the result of dividing x by y

  public boolean isEven(int i) {
    return (i % 2 == 0);

  }
}
